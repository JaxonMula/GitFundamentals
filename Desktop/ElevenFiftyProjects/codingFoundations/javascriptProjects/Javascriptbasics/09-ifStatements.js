// control flow

//* if statements => used to check if a single condition is met.

//*if that 'condition' is met then the code is evaluated to 'true'


/*
syntax:
if(condition){
?    block of code that will be performed if evaluated to true
}
*/

/*
    1. all 'if' statemnets start with the 'if' keyword
    2. the condition that is being checked, its within parenthesis
    3. if the condition evaluates to true, then this code-block will run
*/

//* Examples:

if(10>5){
    console.log("Yep, is is greater than 5!");
}
if(10<5){
    console.log("This code is unreachabole!!");
}